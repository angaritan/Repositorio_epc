package co.com.comcel.vopo;

public class POCodNum {
	private int tmcode;
	private int spcode;
	private int sncode;
	private String tipo;
	public int getTmcode() {
		return tmcode;
	}
	public void setTmcode(int tmcode) {
		this.tmcode = tmcode;
	}
	public int getSpcode() {
		return spcode;
	}
	public void setSpcode(int spcode) {
		this.spcode = spcode;
	}
	public int getSncode() {
		return sncode;
	}
	public void setSncode(int sncode) {
		this.sncode = sncode;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	

}
