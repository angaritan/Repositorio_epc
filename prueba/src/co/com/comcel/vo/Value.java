package co.com.comcel.vo;

public class Value {
	
	Integer id;
	Integer idprice;
	String item_parameter;
	String value;
	String rate_group;
	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	/**
	 * @return the idprice
	 */
	public Integer getIdprice() {
		return idprice;
	}
	/**
	 * @param idprice the idprice to set
	 */
	public void setIdprice(Integer idprice) {
		this.idprice = idprice;
	}
	/**
	 * @return the item_parameter
	 */
	public String getItem_parameter() {
		return item_parameter;
	}
	/**
	 * @param item_parameter the item_parameter to set
	 */
	public void setItem_parameter(String item_parameter) {
		this.item_parameter = item_parameter;
	}
	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(String value) {
		this.value = value;
	}
	/**
	 * @return the rate_group
	 */
	public String getRate_group() {
		return rate_group;
	}
	/**
	 * @param rate_group the rate_group to set
	 */
	public void setRate_group(String rate_group) {
		this.rate_group = rate_group;
	}

}
